-- ABS
SELECT ABS(-2) -- 2

-- SQRT
SELECT SQRT(4) -- 2

-- SQUARE
SELECT SQUARE(2) -- 4

-- POWER
SELECT POWER(2, 3) -- 8

-- ROUND
SELECT ROUND(3.44, 1) -- 3.40
SELECT ROUND(3.46, 1) -- 3.50

-- FLOOR
SELECT FLOOR(3.44) -- 3
SELECT FLOOR(3.46) -- 3

-- PI
SELECT PI() -- 3.14159265358979

-- SIGN
SELECT SIGN(2)  -- 1
SELECT SIGN(0)  -- 0
SELECT SIGN(-2) -- -1
GO


